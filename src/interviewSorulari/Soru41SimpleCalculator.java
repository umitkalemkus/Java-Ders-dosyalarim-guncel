package interviewSorulari;

public class Soru41SimpleCalculator {

    public static void main(String[] args) {


        for (int i = 0; i <=10 ; i++) {
            for (int j = 0; j <=10 ; j++) {

                System.out.println(i+"X"+j +"="+ i*j);

            }
            System.out.println();
        }











    }


}
