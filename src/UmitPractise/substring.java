package UmitPractise;

public class substring {
    public static void main(String[] args) {

        String isim = "Galatasaray turkiye'nin en cok sampiyon olan klubudur.";

        System.out.println(isim.substring(0,11));
        System.out.println(isim.substring(0));


        String isim1 = isim.substring(0,11);
        String isim2 = isim.substring(22,30);
        String isim3 = isim.substring(17,23);
        String isim4 = isim.substring(9,21);
        System.out.println(isim1);
        System.out.println(isim2);
        System.out.println(isim3);
        System.out.println(isim4);














    }





}
